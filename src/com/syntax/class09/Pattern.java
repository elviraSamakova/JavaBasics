package com.syntax.class09;

public class Pattern {

	public static void main(String[] args) {
		for (int i=1; i<=10;i++) {
			for (int y=1; y<=10; y++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("-------------Pattern2----------------");
		
		for (int z=1; z<=5;z++) {
			for (int w=1; w<=10; w++) {
				System.out.print("* ");
			}
			System.out.println();
		}
	}

}
