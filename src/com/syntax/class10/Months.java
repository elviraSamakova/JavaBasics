package com.syntax.class10;

public class Months {

	public static void main(String[] args) {
		String[][] months= {
				{"January", "February","December"},
				{"March", "April","May"},
				{"June","July","August"},
				{"September","October","November"},
		};

	System.out.println(months[0][0]+" is Winter month.");
	System.out.println(months[1][1]+" is Spring month.");
	System.out.println(months[2][0]+" is Summer month.");
	System.out.println(months[3][2]+" is Autumn month.");

	
	}

}
