package com.syntax.class07;

public class WhileVsDo {

	public static void main(String[] args) {
		int num=11;
		
		while (num<=5) {
			System.out.println("Hello");
			num++;
			
		}
System.out.println("-----using while----------");
	int num2=11;
	
	do {
		System.out.println("Hello");
		num2++;
	}while (num2<=5);
	
	
	}

}
