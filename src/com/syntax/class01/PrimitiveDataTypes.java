package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
		
		//8 primitive datatypes
		//whole numbers --> byte, short, int,long
		
		byte variableOne=127;
		short variableTwo=32676;
		int var3=1000;
		long var_4=1000000009900l;
		
		//decimal numbers
		float v_5=12.9999f;
		double v_6=132.87553766877569;
		
		//single character
		char anyName='F';
		
		//yes or no
		boolean hungry=true;
		boolean cold=false;
		
		System.out.println(v_5);
		
		
	}

}
